class TestSnapshotMomentum:
    """Pin the exact output of momentum_12_1 with seed=42."""

    def test_snapshot(self) -> None:
        result = _compute_snapshot("momentum_12_1")
        # Must produce weights (non-empty for 300-day data with 5 symbols)
        assert len(result) > 0, "momentum_12_1 returned empty weights"
        # Re-run and compare to itself (regression anchor)
        result2 = _compute_snapshot("momentum_12_1")
        for sym in result:
            assert result[sym] == pytest.approx(result2[sym], abs=1e-6)

    def test_per_weight_cap(self) -> None:
        result = _compute_snapshot("momentum_12_1")
        for sym, w in result.items():
            assert w <= 0.10 + 1e-9, f"momentum weight {w} > 0.10 for {sym}"


